    private void populateSchoolComboBox(JComboBox<String> School) {
        Set<String> schoolH = new HashSet<>();
        BufferedReader reader = null;

        try {
            reader = new BufferedReader(new FileReader("course_list.txt"));
            String line;
            // Skip the header line
            reader.readLine();
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length >= 4) { // 
                    String school = parts[2].trim();
                    schoolH.add(school);

                }
            }
        } catch (IOException e) {
            e.printStackTrace(); // Handle file reading errors
        } finally {
            if (reader != null) {
                try {
                    reader.close();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
        }
      
        

        // Update the combo box with unique study levels
        cboSchool.setModel(new DefaultComboBoxModel<>(schoolH.toArray(new String[0])));
    }
    
