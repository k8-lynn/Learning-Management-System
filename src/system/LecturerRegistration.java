      private String generateNextStudentID() {
        try {
            
            Scanner scanner = new Scanner(new File("lecturer_list.txt"));
            
            Set<String> existingIDs = new HashSet<>();
            while (scanner.hasNextLine()) {
                String line = scanner.nextLine();
                
                String[] parts = line.split(",");
                String lecturerID = parts[0].trim();
                existingIDs.add(lecturerID);
            }
            scanner.close();

            
            int number = 1;
            while (existingIDs.contains("LT" + String.format("%03d", number))) {
                number++;
            }
            String nextStudentID = "LT" + String.format("%03d", number);
            return nextStudentID;
        } catch (FileNotFoundException e) {
            
            System.out.println("File not found: student.txt");
            e.printStackTrace();
        }
        return "L002"; 
    }
    private void loadData() {
        
        File file = new File("lecturer_list.txt");
        if (!file.exists()) {
            try {
                file.createNewFile(); // Create the file if it doesn't exist
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
        try (BufferedReader reader = new BufferedReader(new FileReader(file))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length == 10) {
                    
                    ConcreteLecturer  lecturer = new ConcreteLecturer (parts[0], parts[1], parts[2], parts[3], parts[4], parts[5], parts[6], parts[7], parts[8], parts[9]);
                }
                
               
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    } 

    private void saveData() {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("lecturer_list.txt", true))) {
            for (Lecturer lecturer : lecturers) {
                boolean duplicate = false;
                try (BufferedReader reader = new BufferedReader(new FileReader("lecturer_list.txt"))) {
                    String line;
                    while ((line = reader.readLine()) != null) {
                        if (line.equals(lecturer.toString())) {
                            duplicate = true;
                            break;
                        }
                    }
                } catch (IOException e) {
                    e.printStackTrace();
                }
                if (!duplicate) {
                    writer.write(lecturer.toString());
                    writer.newLine();
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    
